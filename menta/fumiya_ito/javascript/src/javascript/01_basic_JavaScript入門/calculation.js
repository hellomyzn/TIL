// 加算演算子 (+) を用いると、数値の足し算ができます。
var result = 1 + 2
console.log(result) // 3

// 減算演算子 (-) を用いると、数値の引き算ができます。
var result = 5 - 3
console.log(result) // 2

// 乗算演算子 (*) を用いると、数値の掛け算ができます。
var result = 2 * 3
console.log(result) // 6

// 除算演算子 (/) を用いると、数値の割り算の商を求めることができます。
var result = 9 / 3
console.log(result) // 3
var result2 = 20 / 3
console.log(result2) // 6.666666666666667

// 剰余演算子 (%) を用いると、数値の割り算の余りを求めることができます。
var result = 9 % 3
console.log(result) // 0
var result2 = 20 % 3
console.log(result2) // 2