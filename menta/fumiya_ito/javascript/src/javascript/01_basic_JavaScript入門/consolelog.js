
// メッセージをWebコンソールに出力する。
console.log('hello world!')

// その他状況によって使い分ける

// console.warn：警告をログに出します。
console.warn('warn') //警告をログに出す

// console.error：エラーメッセージをログに出します。
var error = "This is error"
console.error(error) // エラーメッセージ

// console.debug：変数などの中身を知りたいときに使います。
var debug = "This is debug"
console.debug(debug) // 変数など中身が知りたいとき