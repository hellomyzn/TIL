// 変数

public class MyClass {
    public static void main(String[] args) {

        // p1_1
        int ix, iy;
        ix = 10;
        iy = 20;
        System.out.println("ix="+ix+" iy="+iy);

        // p1_2
        double dx, dy;
        dx = 10.5;
        dy = 20.25;
        System.out.println("dx="+dx+" dy="+dy);

        // p1_2
        short sx, sy;
        sx = 0x10;
        sy = 0x20;
        System.out.println("sx="+sx+" sy="+sy);
    }
}
